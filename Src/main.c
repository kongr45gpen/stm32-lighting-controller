/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */
  

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */
    // SysTick is handled by FreeRTOS, so it mustn't be called, as the FreeRTOS memory is not initialized
    SysTick->CTRL &= ~SysTick_CTRL_ENABLE_Msk;
  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */
    // Since SystemClock_Config reinitializes the system tick, disable it again
    SysTick->CTRL &= ~SysTick_CTRL_ENABLE_Msk;

    // Enable the DMA1 clock, since the order of initialization of STM32CubeMX is wrong
    __HAL_RCC_DMA1_CLK_ENABLE();
  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART3_UART_Init();
  MX_TIM2_Init();
  MX_TIM3_Init();
  MX_TIM4_Init();
  MX_USART2_UART_Init();
  MX_SPI1_Init();
  MX_TIM1_Init();
  MX_TIM16_Init();
  MX_RNG_Init();
  MX_DMA_Init();
  MX_TIM15_Init();
  MX_MDMA_Init();
  MX_TIM17_Init();
  MX_SPI3_Init();
  /* USER CODE BEGIN 2 */
    HAL_UART_Transmit(&huart3, (uint8_t *) "Hello welrd\r\n", strlen("Hello welrd\r\n"), HAL_MAX_DELAY);

    // Workaround for a bug in the STM32H7 HAL library, which does not properly handle error interrupts for the UART3
    // peripheral. Error interrupts are apparently always enabled when Multi Buffer Communication is enabled, but this
    // is not considered in the HAL interrupt handler. As the interrupt handler bypasses the interrupt flag clear,
    // the interrupt function runs in an infinite loop and halts the program. Here we enable the error interrupt flag
    // for multi-buffer communication, so that HAL thinks it's enabled.
    LL_USART_EnableIT_ERROR(USART3);


  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
    // Initialize the universe
    temporaryBlackout();

    xPwmEventGroupHandle = xEventGroupCreate();
    xButtonEventGroupHandle = xEventGroupCreate();
    xSerialEventGroupHandle = xEventGroupCreate();
    xSerialReceiveBufferHandle = xStreamBufferCreate(2048, 1);
    xErrorQueueHandle = xQueueCreate(1, ERROR_MESSAGE_SIZE);

    xTaskCreate(pingTask, "ping", 500, NULL, 0, NULL);
    xTaskCreate(dmxTask, "dmx", 500, NULL, 5, &dmxTaskHandle);
    xTaskCreate(pwmTask, "PWM", 1000, NULL, 4, &pwmTaskHandle);
    xTaskCreate(displayTask, "display", 10000, NULL, 3, NULL);
    xTaskCreate(addressableTask, "address", 1000, NULL, 4, NULL);
    xTaskCreate(serialReadTask, "serialR", 1000, NULL, 2, NULL);
    xTaskCreate(wirelessTask, "wireles", 1000, NULL, 2, NULL);

    // Re-enable the system clock
    SysTick->CTRL |= SysTick_CTRL_ENABLE_Msk;

    vTaskStartScheduler();
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  /* USER CODE END 3 */
}

